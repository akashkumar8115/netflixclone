var i = 0;
function showi() {
    if (i == 0) {
        document.getElementById("showupi").style.display = "block";
        document.getElementById("crossi").classList.toggle("rotatedi");
        i++
    }
    else {
        document.getElementById("showupi").style.display = "none";
        document.getElementById("crossi").classList.toggle("rotatedi");
        i--;
    }
}


